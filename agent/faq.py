_COMPLEX_INDICATORS: list[str] = [
    "하기 전에", "이전에", "이후에", "하려면", "할 때", "경우에",
    "필요한가요", "필요합니까", "필요한지", "동시에", "먼저",
    "그리고", "뿐만 아니라", "관련이 있", "어떤 영향", "납부",
    "확인이", "과 함께", "와 함께", "둘 다", "모두 필요",
    "비교", "차이", "어떻게 달라", "연관",
]


def _is_complex_question(question: str) -> bool:
    """복합·조건부·비교 질문이면 True를 반환한다."""
    q = question.lower()
    return any(ind in q for ind in _COMPLEX_INDICATORS)


